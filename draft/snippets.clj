(import [org.joda.time DateTime])
(import [org.joda.time.format DateTimeFormat])
(def dtFmt (DateTimeFormat/forPattern "YYYY-MM-dd HH:mm:ss"))
(def t "2009-06-15 04:01:00")
(.parseDateTime dtFmt t)
